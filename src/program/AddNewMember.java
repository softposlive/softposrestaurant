public void ProcessEndDate() {
        Date TempDate1 = new Date();
        int intday;
        String TempYear;
        String TempDate;
        String TempMonth;
        try {
            TempDate1 = ShowDatefmt.parse(M_Begin.getText());
            TempYear = GetYear.format(TempDate1);
            TempDate = GetDate.format(TempDate1);
            TempMonth = GetMonth.format(TempDate1);
            int newYear = (Integer.parseInt(TempYear)) + 2;
            M_End.setText(TempDate + "/" + TempMonth + "/" + newYear);
            Calendar cal = PUtility.StrToCalendar(M_End.getText());
            cal.set(Calendar.DAY_OF_MONTH, DateUtilities.getMaxDay((GregorianCalendar) cal));
            intday = DateUtilities.getMaxDay((GregorianCalendar) cal);
            TempDate = DayFmt.format(intday);
            M_End.setText(TempDate + "/" + TempMonth + "/" + newYear);
            Calendar cal2 = Calendar.getInstance();
            cal2.setTime(TempDate1);
            cal2.add(Calendar.DAY_OF_MONTH, 44);
            M_NewEnd.setText("วันที่หมดอายุบัตรชั่วคราว : " + ShowDatefmt.format(cal2.getTime()));
        } catch (Exception e) {
        }

    }

