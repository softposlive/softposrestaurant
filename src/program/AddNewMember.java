    public Boolean ChkValidDate() {
        Boolean RetVal = true;

        if (!PUtility.ChkDate(M_Brithday.getText())) {
            M_Brithday.requestFocus();
            RetVal = false;
        }
        if (!PUtility.ChkDate(M_Begin.getText())) {
            M_Begin.requestFocus();
            RetVal = false;
        }
        if (!PUtility.ChkDate(M_End.getText())) {
            M_End.requestFocus();
            RetVal = false;
        }

        if (M_Name.getText().length() == 0) {
            PUtility.ShowMsg("กรุณาป้อนชื่อสมาชิก...");
            M_Name.requestFocus();
            RetVal = false;
        }
        if (M_HomeTel.getText().length() == 0&&M_OfficeTel.getText().length() == 0&&M_Mobile.getText().length() == 0) {
            PUtility.ShowMsg("กรุณาป้อนเบอร์โทรศัพท์สมาชิกอย่างน้อย 1 หมายเลข...");
            M_HomeTel.requestFocus();
            RetVal = false;
        }
        String TempCode = M_Code.getText();
        char[] pass = M_Barcode.getPassword();
        String TempBarCode = "";
        for (int i = 0; i < pass.length; i++) {
            TempBarCode = TempBarCode + pass[i];
        }
        if ((TempCode.length()>0) && (!M_Code.getText().equals(""))) {
        if ((TempBarCode.length() == 13)) {
            String TMemCode = TempBarCode.substring(5, 12);
            if (TMemCode.equals(M_Code.getText())) {
                if (!PUtility.CheckEAN13Format(TempCode)) {
                    MSG.WAR_MSG(this, "รูปแบบรหัสบาร์โค๊ดไม่ถูกต้อง...");
                    M_Barcode.requestFocus();
                    RetVal = false ;
                }
            } else {
                MSG.WAR_MSG(this, "รูปแบบรหัสบาร์โค๊ดไม่ถูกต้อง...");
                M_Barcode.requestFocus();
                RetVal = false ;
            }
        } else {
            //PUtility.ShowMsg("รูปแบบรหัสบาร์โค๊ดไม่ถูกต้อง...");
            MSG.WAR_MSG(this, "รูปแบบรหัสบาร์โค๊ดไม่ถูกต้อง...");
            M_Barcode.requestFocus();
             RetVal = false ;
        }
    } else {
        MSG.WAR_MSG(this, "รูปแบบรหัสบาร์โค๊ดไม่ถูกต้อง...");
        M_Barcode.requestFocus();
        RetVal = false ;

    }
        return RetVal;
    }

